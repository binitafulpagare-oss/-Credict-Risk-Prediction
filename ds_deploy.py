import streamlit as st
import pandas as pd
import numpy as np
import joblib

model = joblib.load("credit_risk_model.pkl")

st.set_page_config(page_title="Credit Risk Prediction", page_icon="🏦")

st.title("🏦 Credit Risk Prediction with Explainable AI")
st.write("Predict whether a loan applicant is Good or Bad Risk.")

status_account = st.selectbox("Status Account", ["< 0 DM", "0 to < 200 DM", "no checking account"])
month_duration = st.number_input("Month Duration", 1, 72, 12)
residence_since = st.selectbox(
    "Residence Since (Years)",
    [1, 2, 3, 4]
)
credit_history = st.selectbox("Credit History", [
    "critical account/ other credits existing (not at this bank)",
    "existing credits paid back duly till now",
    "delay in paying off in the past"
])
purpose = st.selectbox("Purpose", ["radio/television", "education", "furniture/equipment", "car (new)", "car (used)"])
credit_amount = st.number_input("Credit Amount", 100, 20000, 2000)
status_savings = st.selectbox("Status Savings", ["< 100 DM", "100 to < 500 DM", "unknown/ no savings account"])
years_employment = st.selectbox("Years Employment", ["unemployed", "1 to < 4 years", "4 to < 7 years", ">= 7 years"])
payment_to_income_ratio = st.slider("Payment to Income Ratio", 1, 4, 2)
status_and_sex = st.selectbox("Status and Sex", ["male : single", "female : divorced/separated/married", "male : divorced/separated"])
secondary_obligor = st.selectbox("Secondary Obligor", ["none", "guarantor"])
collateral = st.selectbox("Collateral", ["none", "car", "real estate", "savings agreement/life insurance"])
age = st.number_input("Age", 18, 80, 30)
other_installment_plans = st.selectbox("Other Installment Plans", ["none", "bank", "stores"])
housing = st.selectbox("Housing", ["own", "rent", "for free"])
n_credits = st.slider("Number of Credits", 1, 4, 1)
job = st.selectbox("Job", [
    "skilled employee/ official",
    "unskilled - resident",
    "management/ self-employed/highly qualified employee"
])
n_guarantors = st.slider("Number of Guarantors", 1, 2, 1)
telephone = st.selectbox("Telephone", ["none", "yes, registered under the customers name"])
is_foreign_worker = st.selectbox("Foreign Worker", ["yes", "no"])

credit_per_month = credit_amount / month_duration

if age <= 25:
    age_group = "18-25"
elif age <= 35:
    age_group = "26-35"
elif age <= 45:
    age_group = "36-45"
elif age <= 60:
    age_group = "46-60"
else:
    age_group = "60+"

high_credit_amount = 1 if credit_amount > 2319 else 0

input_data = pd.DataFrame({
    "status_account": [status_account],
    "month_duration": [month_duration],
    "credit_history": [credit_history],
    "residence_since": [residence_since], 
    "purpose": [purpose],
    "credit_amount": [credit_amount],
    "status_savings": [status_savings],
    "years_employment": [years_employment],
    "payment_to_income_ratio": [payment_to_income_ratio],
    "status_and_sex": [status_and_sex],
    "secondary_obligor": [secondary_obligor],
    "collateral": [collateral],
    "age": [age],
    "other_installment_plans": [other_installment_plans],
    "housing": [housing],
    "n_credits": [n_credits],
    "job": [job],
    "n_guarantors": [n_guarantors],
    "telephone": [telephone],
    "is_foreign_worker": [is_foreign_worker],
    "credit_per_month": [credit_per_month],
    "age_group": [age_group],
    "high_credit_amount": [high_credit_amount]
})
print(input_data.columns.tolist())
print(model.feature_names_in_)
required_cols = model.feature_names_in_
missing_cols = set(required_cols) - set(input_data.columns)

print("Missing Columns:", missing_cols)
if st.button("Predict Credit Risk"):
    prediction = model.predict(input_data)[0]
    probability = model.predict_proba(input_data)[0][1]

    st.subheader("Prediction Result")

    if prediction == 1:
        st.error("Bad Credit Risk / High Default Chance")
    else:
        st.success("Good Credit Risk / Low Default Chance")

    st.write("Default Probability:", round(probability * 100, 2), "%")
